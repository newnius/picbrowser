    //开始抓取或者停止当前工作
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            //结束加载
            if (jButton1.getText().equalsIgnoreCase("stop")) {
                if (ptgo != null && ptgo.isAlive()) {
                    ptgo.state = false;
                }
                jButton1.setText("go");
                jLinfo.setText("正在暂停抓取图片");
                return;
            }

            //在开始新一轮抓取之前，先清空窗口
            jLinfo.setText("正在初始化窗口");
            jPanel2.removeAll();
            jPanel2.updateUI();
            //开始加载

            jButton1.setText("stop");
            String str = address.getText();
            if (!(str.startsWith("http://") || str.startsWith("https://"))) {
                str = "http://" + str;
            }
            Go go = new Go();
            go.init(str, jPanel2);
            ptgo = go;
            go.start();
            jLinfo.setText("正在访问网址：" + str);
        } catch (Exception e) {
            System.out.println("wrong at line:256");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

