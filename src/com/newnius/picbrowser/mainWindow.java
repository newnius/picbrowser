    //将当前网址添加到收藏夹或者打开收藏夹网址
    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        if (permormer == true) {
            try {
                JComboBox com = (JComboBox) evt.getSource();
                String str = (String) com.getSelectedItem();

                switch (str) {
                    case "添加到收藏夹":
                        //添加操作
                        if (url == null || title == null) {
                            break;
                        }
                        if(SQLManager.addUrl(url, title.substring(0, min(title.length(), 48))))jLinfo.setText("添加到收藏夹成功");
                        else jLinfo.setText("网址已存在");
                        updateUrl();
                        break;
                    default:
                        String[] localUrl = str.split(",", 3);
                        //localUrl[0] 是id号

                        String res = SQLManager.getUrl(Integer.parseInt(localUrl[0]));
                        address.setText(res);
                        jButton1ActionPerformed(evt);
                        break;
                }
            } catch (Exception e) {
                System.out.println("wrong at 197");
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

//负责下拉框的更新
    private boolean updateUrl() {
        try{
        permormer = false;
        ArrayList<MyUrl> list = SQLManager.init();
        if (list.isEmpty()) {
            permormer = true;
            return false;
        }
        Iterator<MyUrl> it = list.iterator();

        jComboBox1.removeAllItems();
        jComboBox1.addItem("添加到收藏夹");

        while (it.hasNext()) {
            MyUrl localMyUrl = it.next();
            jComboBox1.addItem(localMyUrl.id + "," + localMyUrl.url + "," + localMyUrl.title);
        }
        jPanel1.updateUI();
        permormer = true;
        }catch(Exception e){
            System.out.println("wrong at line:211");
        }
        return true;
    }

